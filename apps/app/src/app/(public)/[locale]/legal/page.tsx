import type { Metadata } from "next";
import { LegalClient } from "@/components/pages/LegalClient";
import { getTranslations, getTranslationValue } from "@/lib/get-translations";
import { PROJECT_SETTINGS } from "@/settings";

export async function generateMetadata(): Promise<Metadata> {
  const translations = await getTranslations();
  const title = getTranslationValue(translations, "pages.legal.title") || "Legal Information";
  const description = getTranslationValue(translations, "pages.legal.description") || "";

  return {
    title: `${title} | ${PROJECT_SETTINGS.name}`,
    description,
  };
}

export default async function LegalPage() {
  const translations = await getTranslations();
  const title = getTranslationValue(translations, "pages.legal.title") || "Legal Information";
  const description = getTranslationValue(translations, "pages.legal.description") || "";

  return <LegalClient title={title} description={description} />;
}
