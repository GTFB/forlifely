// Category slug mapping
const CATEGORY_SLUG_MAP: Record<string, string> = {
  'Marketing': 'marketing',
  'marketing': 'marketing',
  'Goods': 'goods',
  'good': 'goods',
  'goods': 'goods',
  'Flowers': 'flowers',
  'flowers': 'flowers',
}

// Function to generate seed data
function generateSeedData(): SeedData {
  return {
    __meta__: seedMeta,
    taxonomy: [
      { entity: 'category', name: 'marketing', title: 'Marketing', sort_order: 1 },
      { entity: 'category', name: 'goods', title: 'Goods', sort_order: 2 },
      { entity: 'category', name: 'flowers', title: 'Flowers', sort_order: 3 },
    ],
    products: productTemplates.map((template, index) => {
      const slug = CATEGORY_SLUG_MAP[template.category] || 'goods'
      return {
        uuid: crypto.randomUUID(),
        paid: generateAid('p'),
        title: JSON.stringify({ en: template.title }),
        category: slug,
        is_public: 1,
        statusName: 'PUBLISHED',
        order: (index + 1) * 100, // Генерирует 100, 200, 300...
        data_in: JSON.stringify({
          price: template.price,
          image: template.image,
          description: template.description,
          weight: template.weight,
          topSales: template.topSales ?? false,
          sku: template.sku,
          attributes: {
            ...(template.country && { 
              country: typeof template.country === 'string' 
                ? { ru: template.country, en: template.country }
                : template.country
            }),
            ...(template.length && { 
              length: typeof template.length === 'string'
                ? { ru: template.length, en: template.length }
                : template.length
            }),
            ...(template.weight && { 
              weight: typeof template.weight === 'string'
                ? { ru: template.weight, en: template.weight }
                : template.weight
            }),
            ...(template.vbn && { 
              vbn: typeof template.vbn === 'string'
                ? { ru: template.vbn, en: template.vbn }
                : template.vbn
            }),
          },
        }),
      }
    }),
  }
}

